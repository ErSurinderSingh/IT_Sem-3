package AithmeticOp;

public class ArithmeticOperators {

    public int addition(int a, int b) {
        return a + b;
    }

    protected int subtarction(int a, int b) {
        return a - b;
    }

    private int multiplication(int a, int b) {
        return a * b;
    }

    int division(int a, int b) {
        return a / b;
    }
}
